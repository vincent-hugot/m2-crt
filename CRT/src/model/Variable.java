package model;

public class Variable {
	private String name;

	/**
	 * @param name
	 */
	public Variable(String name) {
		super();
		this.name = name;
	}

	/**
	 * 
	 */
	public Variable() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
