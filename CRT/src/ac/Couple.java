package ac;

import model.Variable;

public class Couple {
	private Variable V1;
	private Variable V2;
	
	
	public Couple() {
		super();
	}
	public Variable getV1() {
		return V1;
	}
	public void setV1(Variable v1) {
		V1 = v1;
	}
	public Variable getV2() {
		return V2;
	}
	public void setV2(Variable v2) {
		V2 = v2;
	}
}
